const RGBtoHSL = (color) => {
  const bigint = parseInt(color.substr(1), 16);
  let r = (bigint >> 16) & 255;
  let g = (bigint >> 8) & 255;
  let b = bigint & 255;

  r /= 255;
  g /= 255;
  b /= 255;
  
  const max = Math.max(r, g, b);
  const min = Math.min(r, g, b);
  let h, s, l = (max + min) / 2;

  if (max === min) {
    h = s = 0; // achromatic
  } else {
    const d = max - min;
    s = l > 0.5 ? d / (2 - max - min) : d / (max + min);
    switch (max) {
      case r: h = (g - b) / d + (g < b ? 6 : 0); break;
      case g: h = (b - r) / d + 2; break;
      case b: h = (r - g) / d + 4; break;
      default: break;
    }
    h /= 6;
  }

  return [h, s, l];
}

export const formattedColor = (color, percent) => {
  const hsl = RGBtoHSL(color);
  return `hsl(${Math.round(hsl[0]*360)}, ${Math.round(hsl[1]*100)}%, ${percent}%)`
}